package org.ut.biolab.medsavant.shard.mapping;

/**
 * Hibernate mapping property representation.
 * 
 * @author <a href="mailto:mirocupak@gmail.com">Miroslav Cupak</a>
 * 
 */
public class MappingProperty {

    private static final String ID_TEMPLATE = "<id name=\"%s\" column=\"%s\" type=\"%s\" />";
    private static final String PROPERTY_TEMPLATE = "<property name=\"%s\" column=\"%s\" type=\"%s\" />";

    private String name = "";
    private String column = "";
    private String type = "";
    private boolean id = false;

    public MappingProperty() {
    }

    public MappingProperty(String name, String column, String type, boolean id) {
        super();
        this.name = name;
        this.column = column;
        this.type = type;
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getColumn() {
        return column;
    }

    public void setColumn(String column) {
        this.column = column;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public boolean isId() {
        return id;
    }

    public void setId(boolean id) {
        this.id = id;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((column == null) ? 0 : column.hashCode());
        result = prime * result + (id ? 1231 : 1237);
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        result = prime * result + ((type == null) ? 0 : type.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        MappingProperty other = (MappingProperty) obj;
        if (column == null) {
            if (other.column != null)
                return false;
        } else if (!column.equals(other.column))
            return false;
        if (id != other.id)
            return false;
        if (name == null) {
            if (other.name != null)
                return false;
        } else if (!name.equals(other.name))
            return false;
        if (type == null) {
            if (other.type != null)
                return false;
        } else if (!type.equals(other.type))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return (isId() ? String.format(ID_TEMPLATE, name, column, type) : String.format(PROPERTY_TEMPLATE, name, column, type));
    }

}
